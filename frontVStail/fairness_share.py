import matplotlib.pyplot as plt
import numpy as np
print("plot")

data1 = np.loadtxt('share_tail_5s/throughputA.plotme')
data2 = np.loadtxt('share_tail_5s/throughputB.plotme')
data3 = np.loadtxt('share_tail_5s/throughputC.plotme')
data4 = np.loadtxt('share_tail_5s/throughputD.plotme')
data5 = np.loadtxt('share_tail_5s/throughputE.plotme')

plt.figure(dpi=400)
plt.plot(data1[2:, 0], data1[2:, 1]/1000, 'cornflowerblue', label="Flow 1", linewidth=1)
plt.plot(data2[2:, 0], data2[2:, 1]/1000, 'indianred', label="Flow 2", linewidth=1)
plt.plot(data3[2:, 0], data3[2:, 1]/1000, 'darkkhaki', label="Flow 3", linewidth=1)
plt.plot(data4[2:, 0], data4[2:, 1]/1000, 'mediumpurple', label="Flow 4", linewidth=1)
plt.plot(data5[2:, 0], data5[2:, 1]/1000, 'lightskyblue', label="Flow 5", linewidth=1)
plt.legend()
plt.title('DCTCP Tail mark Fairness And Convergence')
plt.xlabel('Time (S)')
plt.ylabel('Throughput (Gbps)')
x_ticks = np.arange(0, 45.1, 5)
plt.xticks(x_ticks)
y_ticks = np.arange(0, 1.1, 0.1)
plt.yticks(y_ticks)
plt.show()